CREATE TABLE USUARIO (
    COD_USER INT PRIMARY KEY IDENTITY(1,1),
    CPF NVARCHAR(11) unique null,
    NOME NVARCHAR(200),
    DATANAC DATE,
    RG NVARCHAR(8) unique,
    PASSAPORT NVARCHAR(20) unique null,
    STATUS BIT
);


CREATE TABLE ALUNO (
    COD_ALUNO INT PRIMARY KEY IDENTITY(1,1),
    RA INT unique,
    COD_USER INT
);


CREATE TABLE FUNCIONARIO (
    COD_FUNC INT PRIMARY KEY IDENTITY(1,1),
    SALARIO DECIMAL(10,2),
    HORAEXTRA INT,
    COD_USER INT
);


CREATE TABLE DEPARTAMENTO (
    COD_DEPARTAMENTO INT PRIMARY KEY IDENTITY(1,1),
    NOME NVARCHAR(200)
);


CREATE TABLE PROFESSOR (
    COD_PROF INT PRIMARY KEY IDENTITY(1,1),
    GRADUACAO NVARCHAR(200),
    COD_DEPARTAMENTO INT not null,
    COD_FUNC INT
);


CREATE TABLE COORDENADOR (
    COD_COORDENADOR INT PRIMARY KEY IDENTITY(1,1),
    COD_FUNC INT,
    ACRECIMOSALARIAL DECIMAL(10,2) null
);


CREATE TABLE RESPONSAVEL (
    COD_DEPARTAMENTO INT,
    COD_COORDENADOR INT,
    PRIMARY KEY (COD_DEPARTAMENTO, COD_COORDENADOR)
);


CREATE TABLE DEP_PROF (
    COD_DEPPROF INT PRIMARY KEY IDENTITY(1,1),
    COD_PROF INT NULL,
    COD_DEPARTAMENTO INT NULL
);


CREATE TABLE MATERIA (
    COD_MATERIA INT PRIMARY KEY IDENTITY(1,1),
    NOME NVARCHAR(200),
    EXIGENCIAS NVARCHAR(200)
);


CREATE TABLE PROFESSOR_MATERIA (
    COD_PROFESSORMATERIA INT PRIMARY KEY IDENTITY(1,1),
    COD_PROF INT,
    COD_MATERIA INT
);


CREATE TABLE CURSO (
    COD_CURSO INT PRIMARY KEY IDENTITY(1,1),
    NOME NVARCHAR(200),
    COD_DEPARTAMENTO INT
);


CREATE TABLE CURSO_MATERIA (
    COD_CURSOMATERIA INT PRIMARY KEY IDENTITY(1,1),
    COD_CURSO INT,
    COD_MATERIA INT
);


CREATE TABLE ALUNO_MATERIA (
    COD_MATERIAALUNO INT PRIMARY KEY IDENTITY(1,1),
    COD_ALUNO INT,
    COD_MATERIA INT
);


CREATE TABLE CURSO_ALUNO (
    COD_CURSOALUNO INT PRIMARY KEY IDENTITY(1,1),
    COD_CURSO INT,
    COD_ALUNO INT
);


CREATE TABLE SALA (
    COD_SALA INT PRIMARY KEY IDENTITY(1,1),
    CAPACIDADE INT NOT NULL,
    EQUIPAMENTOS NVARCHAR(200) NULL,
    PREDIO INT NOT NULL
);


CREATE TABLE EXAME (
    COD_EXAME INT PRIMARY KEY IDENTITY(1,1),
    EXIGENCIAS NVARCHAR(200) NULL,
    COD_MATERIA INT NOT NULL,
    QTDALUNO INT,
    COD_SALA INT
);


CREATE TABLE ALUNO_EXAME (
    COD_ALUNOEXAME INT PRIMARY KEY IDENTITY(1,1),
    COD_ALUNO INT,
    COD_EXAME INT
);


CREATE TABLE SECAO (
    COD_SECAO INT PRIMARY KEY IDENTITY(1,1),
    HORARIOINICIO DATETIME NOT NULL,
    HORARIOFIMPREVISTO DATETIME ,
    HORARIOFIM DATETIME,
    STATUS BIT
);


CREATE TABLE SECAO_EXAME (
    COD_SECAOEXAME INT PRIMARY KEY IDENTITY(1,1),
