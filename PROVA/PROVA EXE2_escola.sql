--LISTAR USUARIOS ATIVOS
SELECT NOME, CPF,DATANAC FROM USUARIO WHERE STATUS = 1






--LISTAR OS ALUNOS
SELECT US.NOME AS ALUNOS, AL.RA, US.CPF FROM ALUNO AL
LEFT JOIN USUARIO US ON AL.COD_USER = US.COD_USER







--MOSTRA OQ CADA ALUNO TA CURSANDO
SELECT US.NOME, AL.RA, C.NOME AS CURSO FROM ALUNO AL
LEFT JOIN USUARIO US ON AL.COD_USER = US.COD_USER
LEFT JOIN CURSO_ALUNO CA ON AL.COD_ALUNO = CA.COD_ALUNO
LEFT JOIN CURSO C ON CA.COD_CURSO = C.COD_CURSO







--ALUNOS POR CURSO
SELECT C.NOME, COUNT(CA.COD_ALUNO) AS QTD_ALUNOS FROM CURSO C
INNER JOIN CURSO_ALUNO CA ON  CA.COD_CURSO = C.COD_CURSO
GROUP BY C.NOME;




--MOSTRAR OS COORDENADORES E SEUS DEPARTAMENTOS
SELECT FUNC.COD_FUNC, US.NOME, D.NOME AS DEPARTAMENTO FROM COORDENADOR C
LEFT JOIN FUNCIONARIO FUNC ON C.COD_FUNC = FUNC.COD_FUNC
LEFT JOIN USUARIO US ON US.COD_USER = FUNC.COD_USER
LEFT JOIN RESPONSAVEL RES ON RES.COD_COORDENADOR = C.COD_COORDENADOR
LEFT JOIN DEPARTAMENTO D ON D.COD_DEPARTAMENTO = RES.COD_DEPARTAMENTO



