--CRIAÇÃO TABELAS NORMALIZADAS


CREATE TABLE DEPARTAMENTO(
	IDDEPARTAMENTO INT PRIMARY KEY IDENTITY(1,1),
	NOME NVARCHAR (100),
	DESCRICAO NVARCHAR(200),
	SIGLA NVARCHAR (10)
)


CREATE TABLE CARGO(
	IDCARGO INT PRIMARY KEY IDENTITY(1,1),
	NOME NVARCHAR(100),
	DESCRICAO NVARCHAR(200)
)


CREATE TABLE FUNCIONARIO(
	MATRICULAFUNC INT IDENTITY(1,1),
	NOMEFUNC NVARCHAR(150),
	CPF NVARCHAR(20),
	STATUS NVARCHAR(50),
	TELEFONE NVARCHAR(30),
	EMAIL NVARCHAR (50),
	ENDERECO NVARCHAR(200)
)

CREATE TABLE GERENTE(
	IDGERENTE INT PRIMARY KEY IDENTITY(1,1),
	TITULO NVARCHAR(50)
	)


CREATE TABLE FOLHAPAGAMENTO(
	CODFOLHA INT PRIMARY KEY IDENTITY(1,1),
	MES NVARCHAR(20),
	SALARIOBASE DECIMAL(10,2),
	VALORHREXTRA DECIMAL (10,2),
	TOTALHREXTRA DECIMAL(10,2),
	DESCONTOINSS DECIMAL(10,2),
	SALARIOLIQUIDO DECIMAL(10,2)
)

--ADICIONANDO ATRIBUTOS E FK


ALTER TABLE FUNCIONARIO
ADD
	
	IDDEPARTAMENTO INT,
	IDCARGO INT,


	CONSTRAINT FK_FUNC_DEP FOREIGN KEY(IDDEPARTAMENTO)
	REFERENCES DEPARTAMENTO(IDDEPARTAMENTO),

	CONSTRAINT FK_FUNC_CARGO FOREIGN KEY (IDCARGO)
	REFERENCES CARGO (IDCARGO)

ALTER TABLE FUNCIONARIO
ADD PRIMARY KEY(MATRICULAFUNC);

ALTER TABLE GERENTE
ADD 
	MATRICULAFUNC INT,
	CONSTRAINT FK_GERENTE_FUNC FOREIGN KEY (MATRICULAFUNC)
	REFERENCES FUNCIONARIO (MATRICULAFUNC)


ALTER TABLE DEPARTAMENTO
ADD
	IDGERENTE INT,
	CONSTRAINT FK_DEP_GERENTE FOREIGN KEY (IDGERENTE)
	REFERENCES GERENTE(IDGERENTE)

	
ALTER TABLE FOLHAPAGAMENTO
ADD 
	MATRICULAFUNC INT,
	CONSTRAINT FK_FP_FUNC FOREIGN KEY(MATRICULAFUNC)
	REFERENCES FUNCIONARIO(MATRICULAFUNC)

