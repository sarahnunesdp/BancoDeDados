SELECT 
U.NOME,M.VALOR
FROM USUARIO1 U
JOIN EMPRESTIMO1 E ON U.ID_USUARIO = E.ID_USUARIO
JOIN MULTA1 M ON E.ID_EMPRESTIMO = M.ID_EMPRESTIMO;
