CREATE TABLE USUARIO1 (
ID_USUARIO INT PRIMARY KEY IDENTITY(1,1),
NOME NVARCHAR(200),
EMAIL NVARCHAR (150),
TELEFONE NVARCHAR(50),
ENDERECO NVARCHAR(150)
)

CREATE TABLE AUTOR1 (
ID_AUTOR INT PRIMARY KEY IDENTITY(1,1),
NOME NVARCHAR (200),
NACIONALIDADE NVARCHAR(100)
)

CREATE TABLE LIVRO1(
ID_LIVRO INT PRIMARY KEY IDENTITY(1,1),
TITULO NVARCHAR (200),
EDITORA NVARCHAR(200),
ANO_PUBLICACAO INT,
STS NVARCHAR(100)
)

CREATE TABLE EDITORA1(
ID_EDITORA INT PRIMARY KEY IDENTITY(1,1),
NOME NVARCHAR(200)
)

CREATE TABLE EMPRESTIMO1 (
ID_EMPRESTIMO INT PRIMARY KEY IDENTITY(1,1),
DATA_EMPRETIMO DATE,
DATA_DEVOLUCAO DATE,
DATA_PREV_DEVOLUCAO DATE,
ID_USUARIO INT,
FOREIGN KEY(ID_USUARIO) REFERENCES USUARIO1(ID_USUARIO)
)

CREATE TABLE LIVRO_AUTOR1(
ID_LIVRO_AUTOR INT PRIMARY KEY IDENTITY(1,1),
ID_LIVRO INT,
ID_AUTOR INT,
FOREIGN KEY (ID_LIVRO) REFERENCES LIVRO1(ID_LIVRO),
FOREIGN KEY (ID_AUTOR) REFERENCES AUTOR1(ID_AUTOR)
)

CREATE TABLE EMPRESTIMO_LIVRO1(
ID_EMPRESTIMO_LIVRO INT PRIMARY KEY IDENTITY(1,1),
ID_LIVRO INT,
ID_EMPRESTIMO INT,
FOREIGN KEY (ID_LIVRO) REFERENCES LIVRO1(ID_LIVRO),
FOREIGN KEY (ID_EMPRESTIMO) REFERENCES EMPRESTIMO1(ID_EMPRESTIMO)
)

CREATE TABLE MULTA1 (
ID_MULTA INT PRIMARY KEY IDENTITY(1,1),
VALOR DECIMAL(10,2),
ID_EMPRESTIMO INT,
DATA_PAGAMENTO DATE,
FOREIGN KEY (ID_EMPRESTIMO) REFERENCES EMPRESTIMO1(ID_EMPRESTIMO)
)
 
