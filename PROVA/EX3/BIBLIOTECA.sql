U.NOME, E.ID_EMPRESTIMO,E.DATA_EMPRETIMO,E.DATA_DEVOLUCAO
FROM EMPRESTIMO1 E
JOIN USUARIO1 U ON E.ID_USUARIO = U.ID_USUARIO;
